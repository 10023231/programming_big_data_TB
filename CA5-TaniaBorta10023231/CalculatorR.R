#Applying calculation functions in R

#sumation
sum_num <- function(x,y){
  x + y
}
sum_num(3,3)

#Substraction
sub_num <- function(x,y){
  x - y
}
sub_num(6,3)
#multiplying
mult_num <- function(x,y){
  x * y
}
mult_num(3,3)

#division
div_num <- function(x,y){
  x / y
}
div_num(9,2)

#exponential
exp_num <- function(x,y){
  x ** y
}
exp_num(3,2)

#square root
sqrt_num <- function(x){
  x ** (1/2.0)
}
sqrt_num(25)

#cube
cube_num <- function(x){
  x ** 3
}
cube_num(3)

#power
pow_num <- function(x){
  x ** 2
}
pow_num(3)

#sine
sine <- function(x){
  sin(x*pi/180)
}
sine(3)

#cosine
cosine<- function(x){
   cos(120*pi/180)
}
cosine(3)
